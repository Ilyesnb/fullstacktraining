const mongoose = require("mongoose");

const gameSchema = mongoose.Schema(
  {
    title: {
      type: String,
      required: true,
    },
    category: {
      type: String,
      required: true,
    },
    price: {
      type: Number,
      required: true,
    },
  },
  { timestamps: true }
);

const Game = mongoose.model("game", gameSchema);

module.exports = Game;
